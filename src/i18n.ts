export type TranslationKey = keyof typeof dictionaries.pl;

const fontScaleMap: Record<FontSizePreference, number> = {
  small: 0.92,
  medium: 1,
  large: 1.14,
};

export function translate(
  language: LanguagePreference,
  key: TranslationKey,
  replacements?: Record<string, string | number>,
): string {
  const dict = (dictionaries[language] ?? dictionaries.pl) as Record<string, string>;
  const fallbackDict = dictionaries.pl as Record<string, string>;
  let raw = String(dict[key] ?? fallbackDict[key] ?? key);

  if (!replacements) {
    return raw;
  }

  for (const [token, value] of Object.entries(replacements)) {
    raw = raw.replaceAll(`{${token}}`, String(value));
  }

  return raw;
}

export function useI18n() {
  const { state } = useApp();
  const language = state.settings.language;
  const fontSize = state.settings.fontSize;

  return useMemo(() => {
    const scale = fontScaleMap[fontSize] ?? 1;

    return {
      language,
      fontSize,
      scale,
      t: (key: TranslationKey, replacements?: Record<string, string | number>) =>
        translate(language, key, replacements),
      scaleFont: (value: number) => Math.round(value * scale),
      scaleLineHeight: (value: number) => Math.round(value * scale),
    };
  }, [fontSize, language]);
}

export function getLanguageLabel(
  language: LanguagePreference,
  t: (key: TranslationKey, replacements?: Record<string, string | number>) => string,
) {
  if (language === 'en') return t('languageEnglish');
  if (language === 'uk') return t('languageUkrainian');
  return t('languagePolish');
}

export function getFontSizeLabel(
  size: FontSizePreference,
  t: (key: TranslationKey, replacements?: Record<string, string | number>) => string,
) {
  if (size === 'small') return t('fontSmall');
  if (size === 'large') return t('fontLarge');
  return t('fontMedium');
}
